    private void ModifyHrbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModifyHrbtnActionPerformed
        if(!validateInputs()){
            return;
        }
        try{
            boolean result=UserDAO.updatePwd(jcAllUserId.getSelectedItem().toString(), Password);
            if(result){
                JOptionPane.showMessageDialog(null, "Password Change Successfully","Done", JOptionPane.INFORMATION_MESSAGE);
                
            }
            else{
                JOptionPane.showMessageDialog(null, "Sorry! Could not Change the Password","Unsuccesfull", JOptionPane.ERROR_MESSAGE);
            }
            clearText();
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Db Error","Error!", JOptionPane.ERROR_MESSAGE);   
            ex.printStackTrace();
            
        }

    }//GEN-LAST:event_ModifyHrbtnActionPerformed

