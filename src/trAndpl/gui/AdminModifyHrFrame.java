    private boolean validateInputs(){
        char[]pwd=TxtNewPwd.getPassword();
        char[]conPwd=TxtConfrmPwd.getPassword();
        if(pwd.length<6||conPwd.length<6){
            JOptionPane.showMessageDialog(null, "Password must be atleast 6 characters long!");
            return false;
        }
        Password=String.valueOf(pwd);
        String conpassword=String.valueOf(conPwd);
        if(Password.equals(conpassword)==false){
            JOptionPane.showMessageDialog(null, "Password not matching!");
            return false;
           }
        return true;
    }
    private void clearText(){
        TxtNewPwd.setText("");
        TxtConfrmPwd.setText("");
        jcAllUserId.setSelectedIndex(0);
    }

